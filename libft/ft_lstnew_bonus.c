#include "libft.h"

t_node	*ft_lstnew(void *content)
{
	t_node	*list;

	list = (t_node *)malloc(sizeof(t_node));
	if (!list)
		return (NULL);
	list->content = content;
	list->next = NULL;
	return (list);
}
